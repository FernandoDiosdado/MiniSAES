    //poner todos los grados en los comboBox
    public void setjBoxGrados() {
        ArrayList<Integer> grados = SentenciasSQL.consultarGrados();
        for (int g : grados) {
            
            jComboBoxGradoAT.addItem(String.valueOf(g));
            jComboBoxGradoPT.addItem(String.valueOf(g));
            jComboBoxGradoGI.addItem(String.valueOf(g));
            jComboBoxGradoGA.addItem(String.valueOf(g));
        }
    }

    //actualiza tablas
    public void actualizarTablas() {
        String grupoGI="";
        try{
            grupoGI = "WHERE id_grupoFK='" + jComboBoxGruposGI.getSelectedItem().toString() + "'";
        }catch(Exception ex){
            
        }
        //poner los grupos en los comboBox 
        this.setjBoxGrupos(jComboBoxGradoAT, jComboBoxGruposAT);
        this.setjBoxGrupos(jComboBoxGradoPT, jComboBoxGruposPT);
        //Inicializar todas las tablas
        tablaDatosAlumnos = new TablaAdminAlumno("");
        tablaDatosProfesores = new TablaAdminProfesor("");
        tablaGT = new TablaAdminGT();
        tablaGI = new TablaAdminGI(grupoGI);
        //ponerlas dentro de un panel
        jScrollPaneAT.setViewportView(tablaDatosAlumnos.table);
        jScrollPanePT.setViewportView(tablaDatosProfesores.table);
        jScrollPaneGT.setViewportView(tablaGT.table);
        jScrollPaneGI.setViewportView(tablaGI.table);
        actualizarLabelProfesor();
    }
    
    public void actualizarLabelProfesor(){
        String grupo = "";
        try {
            grupo = "WHERE id_grupoFK='" + jComboBoxGruposGI.getSelectedItem().toString() + "'";
            ArrayList<Profesor> prof = admin.todosProfesores(grupo);
            jLabelNomProfesorGI.setText(prof.get(0).getApPaterno()+" "+ prof.get(0).getApMaterno() +" " + prof.get(0).getNombres());
        } catch (Exception ex) {
            jLabelNomProfesorGI.setText("No tiene profesor");
        }
    }

    //actulizar Grupos de acuerdo al grado seleccionado
    public void setjBoxGrupos(JComboBox grado, JComboBox grupos) {
        String gradoSeleccionado = grado.getSelectedItem().toString();
        ArrayList<String> listaGrupos = admin.obternerGruposPorGrado(gradoSeleccionado);
        grupos.removeAllItems();
        for (String g : listaGrupos) {
            grupos.addItem(String.valueOf(g));
        }
    }

